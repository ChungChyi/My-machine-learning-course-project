
#定义个Class，包含两个数字加法运算的方法

class my_math:
    def __init__(self,num1,num2):
        self.num1 = int(num1)
        self.num2 = int(num2)
    def SumTwoNums(self):
        return self.num1 + self.num2
    def MulTwoNums(self):
        return self.num1 * self.num2
    def SubTwoNums(self):
        return self.num1 - self.num2
    def DivTwoNums(self):
        if self.num2 != 0:
            return self.num1/self.num2
        else:
            return 'The divisor can not be zero!!!'

